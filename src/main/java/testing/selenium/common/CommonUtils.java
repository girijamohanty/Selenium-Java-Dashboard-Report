package testing.selenium.common;

public class CommonUtils {
	private String storyName;
	public void setStoryName(String storyTestCaseName) {
		storyName = storyTestCaseName;
	}
	public String getStoryName() {
		return storyName;
	}
    
	
	 public String generateMultipleLineOutput(String arg)
	 {
		String output ="\n******************************************************************************************************************************\n"
			         + "******************************************************************************************************************************\n"
			         + "*****************************************"+arg+"*******************************************\n"
			         + "******************************************************************************************************************************\n"
			         + "******************************************************************************************************************************";
				 
		return output;
	 }
}
